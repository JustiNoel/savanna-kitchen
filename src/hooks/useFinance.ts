// Financial summary calculations - excludes cancelled orders, counts paid revenue up-to-date
export const useFinancialSummary = () => {
  const queryClient = useQueryClient();
  const { data: invoices } = useInvoices();
  const { data: expenses } = useExpenses();
  const { data: orders } = useQuery({
    queryKey: ['orders-summary'],
    queryFn: async () => {
      const { data, error } = await supabase
        .from('orders')
        .select('total_amount, status, created_at, payment_status')
        .order('created_at', { ascending: false });
      if (error) throw error;
      return data;
    },
    refetchInterval: 15000, // poll every 15s as a safety net
  });

  // Realtime: refresh whenever orders/transactions change
  useEffect(() => {
    const channel = supabase
      .channel('finance-summary-realtime')
      .on('postgres_changes', { event: '*', schema: 'public', table: 'orders' }, () => {
        queryClient.invalidateQueries({ queryKey: ['orders-summary'] });
      })
      .on('postgres_changes', { event: '*', schema: 'public', table: 'financial_transactions' }, () => {
        queryClient.invalidateQueries({ queryKey: ['financial-transactions'] });
        queryClient.invalidateQueries({ queryKey: ['orders-summary'] });
      })
      .on('postgres_changes', { event: '*', schema: 'public', table: 'expenses' }, () => {
        queryClient.invalidateQueries({ queryKey: ['expenses'] });
      })
      .on('postgres_changes', { event: '*', schema: 'public', table: 'invoices' }, () => {
        queryClient.invalidateQueries({ queryKey: ['invoices'] });
      })
      .subscribe();
    return () => { supabase.removeChannel(channel); };
  }, [queryClient]);

  // Exclude cancelled orders entirely. Revenue = any PAID order that isn't cancelled.
  const validOrders = orders?.filter(o => o.status !== 'cancelled') || [];
  const paidOrders = validOrders.filter(o => o.payment_status === 'paid');
  const deliveredOrders = validOrders.filter(o => o.status === 'delivered');

  const grossRevenue = paidOrders.reduce((sum, o) => sum + Number(o.total_amount), 0);
  // Subtract any refunds recorded in financial_transactions (e.g. cancelled-after-paid orders)
  const refundsTotal = useQuery({
    queryKey: ['finance-refunds'],
    queryFn: async () => {
      const { data, error } = await supabase
        .from('financial_transactions')
        .select('amount')
        .eq('type', 'refund');
      if (error) throw error;
      return (data || []).reduce((s, r) => s + Number(r.amount), 0);
    },
    refetchInterval: 15000,
  }).data || 0;
  const totalRevenue = Math.max(0, grossRevenue - refundsTotal);
  const totalExpenses = expenses?.reduce((sum, exp) => sum + Number(exp.amount), 0) || 0;
  const pendingInvoices = invoices?.filter(inv => inv.status === 'pending').length || 0;
  const paidInvoices = invoices?.filter(inv => inv.status === 'paid').length || 0;
  const netProfit = totalRevenue - totalExpenses;

  return {
    totalRevenue,
    grossRevenue,
    refundsTotal,
    totalExpenses,
    netProfit,
    pendingInvoices,
    paidInvoices,
    totalOrders: validOrders.length,
    deliveredOrders: deliveredOrders.length,
    paidOrders: paidOrders.length,
    cancelledOrders: (orders?.length || 0) - validOrders.length,
  };
};
